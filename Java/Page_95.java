public class Page_95 {
    public static void main(String args[]){
        // 데이터형[] 배열이름 = {값 초기화};
        int[] scores = {10,20,30,40,50}; // int 형 scores라는 배열을 선언하고 값을 초기화 한다.
        for (int i = 0; i < scores.length; i++){ // scores.length = 배열의 길이를 저장함
            System.out.println(scores[i]); // System.out.println(); i번째의 값을 출력함
        }
    }
}
