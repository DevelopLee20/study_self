public class Page_49{
    public static void main(String args[]){ // String args[] 와 String[] args 모두 사용 가능
        int i;  // int 정수형
        double f; // double 실수형

        f = 5 / 4;  // 나누기 연산자
        System.out.println(f); // System.out.println();

        f = (double) 5 / 4; // 나누기 연산자 (double) double 형으로 형 변환
        System.out.println(f);

        i = (int) 1.3 + (int) 1.8; // int 형으로 형변환 (int)
        System.out.println(i); // System.out.println();
    }
}