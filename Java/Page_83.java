public class Page_83 {
    public static void main(String args[]){ // String args[] 와 String[] args 는 다르지 않다고 봐도 무관하다.
        for (int i = 0; i < 5; i++){ // for 문에 의한 반복문
            for (int j = 0; j < 5; j++){ // for 문에 의한 2중 반복문
                System.out.println(i+j);
            }
            
        }
    }
}
